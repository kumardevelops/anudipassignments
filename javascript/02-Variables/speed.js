// Write a script to input distance and time and calculate speed in javascript and display all the data in alert box.

"use strict";
let d, t, speed;
d = parseInt(prompt(" Please enter the Distance :"));
t = parseInt(prompt(" Please enter the Time :"));
speed = d / t;
alert("Distance =" + d);
alert("Time =" + t);
alert("Speed =" + speed);
